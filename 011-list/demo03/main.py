#如果想逐个取出列表中的元素，可以使用for循环的，有以下两种做法。

#方法一：

items = ['Python', 'Java', 'Go', 'Kotlin']

for index in range(len(items)):
    print(items[index])
#方法二：

#items = ['Python', 'Java', 'Go', 'Kotlin']

for item in items:
    print(item)


